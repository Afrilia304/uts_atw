						<p>Meskipun banyak artikel yang mengatakan bahaya mengkonsumsi kopi namun tak sedikit juga artikel yang mengatakan manfaat dari mengkonsumsi kopi hitam. Mungkin yang mesti kita perhatikan adalah jenis kopi yang kita konsumsi. Apakah kopi hitam atau kopi susu atau kopi sachet atau kopi kekinian (apapun itu istilahnya)?
						Karena menurut saya, jika kita benar-benar mengkonsumsi kopi hitam segar tanpa campuran gula ataupun susu, maka banyak sekali manfaat yang bisa kita rasakan. Kali ini saya ingin membahas 12 alasan ilmiah kenapa kamu harus minum kopi hitam setiap hari. Sebelumnya, saya ingin memastikan lagi, kopi hitam yang dimaksud di sini adalah kopi hitam murni tanpa campuran gula dan susu. Benar-benar kopi hitam. Apa saja alasannya?</p>
						<p>1. Menyehatkan Fungsi Organ Hati
						Tidak seperti minum alkohol, ternyata minum kopi hitam setiap hari dapat menyehatkan fungsi organ hati kamu. Ini telah terbukti, orang yang minum 3 cangkir kopi hitam atau lebih setiap harinya akan memiliki tingkat kemungkinan terkena sirosis (kondisi rusaknya organ hati akibat jaringan parut) hati 80% lebih rendah.Selain itu, dengan mengkonsumsi jumlah yang sama, maka kemungkinan untuk mengalami kanker hati 40% lebih rendah. Luar biasa bukan? <br><br>

						2. Membuat Lebih Pintar
						Kopi mengandung kafein. Kafein adalah stimulan psikoaktif. Saat kita minum kopi hitam, kafein masuk ke saluran pencernaan kita, lalu masuk ke aliran darah kita. dan akhirnya masuk ke otak kita (proses ini membutuhkan waktu sekitar 30-45 menit). Dan ketika kafein mengenai otak kita, maka kafein akan memblokir salah satu neurotransmitter yang menghambat di otak kita, yaitu Adenosine. Hal ini menyebabkan meningkatnya neurotransmitter lain (norepinefrin dan dopamin) dan menyebabkan neuron di otak kita terbakar lebih cepat. Semua bahan kimia dan neuron ini menyatu untuk meningkatkan mood, energi, memori, waktu responsif dan fungsi kognitif umum di otak kita.
						Itulah sebabnya, kenapa di saat kita minum kopi hitam, konsentrasi kita kembali meningkat. Kamu ngerasain hal yang sama juga kan? <br><br>

						3. Meningkatkan Metabolisme Tubuh sampai 11%
						Ingin menurunkan berat badan? coba konsumsi kopi hitam. Karena ternyata kafein yang ada di dalam kopi terbukti secara langsung sebagai zat yang dapat membakar lemak. Sehingga dapat membantu menurunkan berat badan kita.<br><br>

						Namun bukan berarti hanya dengan minum kopi hitam saja, berat badan kamu langsung turun. Kamu juga harus menyeimbangkannya dengan olahraga yang teratur dan menjaga pola makan ya. Tapi kan setidaknya dengan minum kopi hitam, akan membantu menurunkan berat badan kamu. Berani coba? Ingat ya kopi hitam murni tanpa susu dan gula. Bukan kopi susu atau es kopi kekinian. <br><br>

						4. Memberikan Nutrisi Penting Bagi Tubuh
						Ketika kita minum kopi hitam murni, ternyata ini juga sebagai sumber antioksidan terbesar bagi tubuh kita. Karena beberapa nutrisi penting juga ikut masuk ke dalam tubuh kita, seperti: Vitamin B2, B3, B5, Manga, Magnesium dan Kalium.
						Bahkan menurut sumber, tubuh manusia menyerap lebih banyak nutrisi dari kopi dari pada sumber antioksidan populer lainnya seperti buah-buahan dan sayuran. Masih ragu minum kopi hitam murni?<br><br>

						5. Mengurangi Resiko Diabetes Type 2
						Perlu diingat, kamu tidak akan mendapatkan manfaat ini apabila kandungan kopi yang kamu minum adalah setengah cangkir gula, setengah cangkir susu, setengah cangkir kopi. Tapi jika kamu minum kopi hitam murni tanpa campuran maka penelitian menunjukkan rata-rata 7% penurunan resiko diabetes untuk setiap cangkirnya. Ayo minum kopi hitam murni.<br><br>

						6. Mengurangi Resiko Parkinson
						Tidak seorangpun ingin terkena penyakit neurodegeneratif – khususnya mereka yang anggota keluarganya pernah mengalaminya. Parkinson dikaitkan dengan setetes dopamin. Dan karena kopi hitam dapat meningkatkan kadar dopamin dalam otak kita, maka minum kopi hitam dapat mengurangi kemungkinan terkena parkinson.
						Peminum kopi hitam murni reguler tanpa gula dan susu telah terbukti memiliki peluang berkurangnya terkena parkinson sebesar 32-60%.<br><br>

						7. Mengurangi Depresi dan Membuat Lebih Bahagia
						Ingat, kopi hitam murni mengandung banyak kafein, dan kafein akan meningkatkan dopamin di otak kita. Dopamin umumnya dikenal sebagai “bahan kimia kebahagiaan”. Sehingga masuk akal apabila kita mengkonsumsi sesuatu yang dapat meningkatkan dopamin maka akan membuat kita bahagia dan tidak tertekan. Pernah merasakan hal yang sama juga? Jika belum ganti kopi kamu dengan kopi hitam murni tanpa gula dan susu.
						Orang yang minum 3 cangkir kopi atau lebih memiliki peluang 20% lebih rendah untuk mengalami depresi, dan 50% lebih kecil kemungkinannya untuk bunuh diri. Sesungguhnya minum kopi hitam murni adalah penyelamat hidup, maka ketika kamu menemui orang atau teman yang depresi ajaklah ia minum kopi hitam murni.<br><br>

						8. Menurunkan Resiko Terkena Kanker
						Selain dapat mengurangi resiko terkena kanker hati, karena kopi dapat menyehatkan fungsi organ hati. Ternyata kopi hitam juga dapat mengurangi resiko terkena kanker kolokteral atau kanker usus. Orang yang minum kopi hitam 3 cangkir atau lebih dalam sehari memiliki resiko 15% lebih rendah terkena kanker kolokteral atau kanker usus dan 40% resiko kanker hati lebih rendah.
						Selain itu, kopi juga bisa mengurangi resiko kanker kulit sebesar 20% terutama pada wanita. Yakin gak mau minum kopi hitam murni? <br><br>

						9. Mengurangi Resiko Penyakit Jantung
						Orang yang minum kopi secara teratur memiliki resiko 20% lebih rendah untuk terkena stroke dan umumnya memiliki tingkat penyakit jantung yang lebih rendah. Karena kafein meningkatkan detak jantung kita, makanya sebenarnya kopi baik untuk kesehatan jantung. Hanya saja jenis kopinya saja yang perlu diperhatikan.
						Minum beberapa cangkir kopi sehari memiliki efek yang sama dengan berjalan-jalan, yang akan membuat jantung kita sehat. Tapi jangan dijadikan alasan untuk tidak berolahraga ya. Tetap harus diseimbangi dengan olahraga.<br><br>

						10. Membersihkan Sistem Pencernaan Pada Tubuh
						Ternyata kopi bersifat diuretik (berfungsi untuk membuang kelebihan garam dan air dari dalam tubuh melalui urine). Dengan sering minum kopi, maka kita juga akan sering buang air kecil. Tentu ini akan baik untuk tubuh kita, karena akan menjaga sistem pencernaan tetap bersih. Karena tubuh manusia sering membuang virus berbahaya dan bakteri melalui cara ini. Dan terbukti orang yang sering minum kopi hitam menjadi jarang terkena penyakit. <br><br>

						11. Membuat Lebih Tenang
						Jangankan minum kopi hitam, hanya mencium aroma biji kopi saja membuat kita tenang. Betul tidak? Sebenarnya kopi tersebut telah merubah komposisi protein yang ada di dalam otak yang terkait dengan stres akibat kurang tidur. Hayo ngaku siapa yang pakai biji kopi sebagai pengharum ruangan? Apakah kamu merasakan ketenangan?<br><br>

						12. Mengurangi Resiko Deminsia dan Alzheimer
						Kopi pada umumnya akan meningkatkan daya ingat, karena adanya kafein di beberapa neurotransmitter pada otak. Dengan terus meningkatkan ingatan dari waktu ke waktu selama bertahun-tahun, maka kita akan mengurangi resiko terkena Demensia dan Alzheimer.

						Hal ini terbukti, peminum kopi reguler memiliki 65% pengurangan resiko terkena penyakit neurodegeneratif paling umum (Demensia dan Alzheimer) di Dunia. Yuk minum kopi hitam murni. <br><br>


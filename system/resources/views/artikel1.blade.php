						Sangat banyak sekali manfaat yang bisa kita dapatkan dari mengkonsumsi kopi hitam murni. Hanya saja yang perlu diperhatikan adalah jenis kopi yang kita konsumsi. Sebaiknya konsumsi kopi hitam murni tanpa campuran apapun. Karena sebetulnya yang buat minum kopi menjadi buruk adalah campuran yang ditambahkan pada minuman tersebut.
						Jika kamu tidak bisa minum kopi hitam tanpa gula langsung karena terlalu pahit, kamu bisa mengurangi gula sedikit demi sedikit. Jika hari ini pakai gula 1 sendok makan, besok bisa dikurangi jadi setengah sendok makan. Seterusnya seperti itu sampai kamu terbiasa minum kopi hitam murni tanpa gula dan susu.
						Dan konsumsilah kopi hitam segar, jangan mengkonsumsi kopi sachet (instan), karena kopi sachet (instan) juga mengandung bahan-bahan yang tidak sehat untuk tubuh kamu. Di beberapa jenis kopi sachet (instan) juga mengandung biji jagung, gula, dan susu.
						Jika kamu ingin mendapatkan ke 12 manfaat di atas, maka konsumsilah kopi hitam murni tanpa campuran apapun. Apakah kamu sudah merasakan manfaat seperti di atas? Atau kamu merasakan manfaat selain yang disebutkan di atas? Yuk kita bahas di kolom komentar.</p>

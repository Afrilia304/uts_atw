						Mari kita bahas bersama-sama sebelum kamu menentukan, kamu akan membeli kopi dalam bentuk biji atau bubuk.?</p> <br><br>
						<h5>Kopi dalam Bentuk Bubuk</h5>

						<p>Kenapa kita menggiling (grinding) biji kopi? untuk menghancurkan biji kopi sehingga memiliki permukaan yang lebih lebar dan menghasilkan aroma dan rasa yang lebih baik saat ekstraksi. Meskipun bisa saja kita menyeduh langsung biji kopi tersebut tanpa menggilingnya terlebih dulu, namun akan membutuhkan waktu lama untuk ektraksinya, dan aroma yang didapat tidak akan bisa kita nikmati. <br><br>
						Dengan menggiling biji kopi, permukaan biji kopi akan melebar atau meluas, sehingga akan banyak permukaan kopi yang terkena air. Ini menyebabkan akan lebih banyak senyawa yang ada di dalam kopi tersebut terekstraksi dan larut ke dalam air lebih cepat. Dengan memahami proses dasar ektraksi ini, maka kita akan mengerti pro dan kontra kopi dalam bentuk bubuk. <br>

						Ketika kamu membeli kopi dalam bentuk bubuk, besar kemungkinan kopi tersebut sudah “terpengaruh” oleh lingkungan sekitar (cahaya, udara), apalagi jika kopi tersebut tidak disimpan dan dikemas dengan baik. Pada saat itu, terjadi sebuah proses oksidasi atau proses masuknya gas oksigen ke dalam kopi bubuk tersebut. Hal ini akan membuat kopi bereaksi terhadap minyak alami dan kandungan larutan lain yang dimilikinya sehingga membuat kopi tidak segar lagi. Atau yang biasa kita sebut kopi basi. <br></p>

						<h5>Lalu, apakah membeli kopi dalam bentuk biji selalu lebih baik?</h5>
						<p>Bisa dibilang seperti itu, tapi bisa juga dibilang tidak, kapan dan kenapa?

						Ketika kita menggiling kopi, kita membuat kopi menjadi banyak bagian-bagian kecil. Dan jika kita menggunakan grinder (penggiling) kopi yang berkualitas rendah, maka kita akan mendapatkan hasil gilingan yang tidak konsisten (berbeda ukuran dan bentuknya). <br>

						Kopi bubuk yang tidak konsisten akan membuat proses ektraksi kopi juga menjadi tidak konsisten. Kembali lagi ke proses ektraksi. Ketika proses ektraksi, senyawa dari biji kopi larut ke dalam air dengan laju yang berbeda. Pertama, kita akan mendapatkan asam, kemudian manis, lalu pahit. Sedangkan tujuan kita menyeduh kopi yaitu untuk mendapatkan keseimbangan yang tepat dari semua senyawa tersebut.<br><br>

						Dengan hasil gilingan (grinder) kopi yang konsisten, maka ketika ektraksi kita akan mudah untuk mengontrol rasa dan aroma kopi yang kita seduh, sedangkan jika kita menggiling (grinder) kopi dan hasilnya tidak konsisten, kita akan sulit mengontrol proses ektraksi kopi tersebut. Dengan kata lain, dibutuhkan hasil gilingan kopi yang konsisten untuk mendapatkan keseimbangan aroma dan rasa ketika menyeduh kopi.<br>

						Jadi, balik lagi ke pertanyaan di awal, apakah membeli kopi dalam bentuk biji selalu baik? Iya, jika kamu mempunyai alat penggiling kopi (grinder) yang bagus (konsisten) di rumah. Tapi, bagaimana…<br><br>
						Jika tidak punya?
						Maka pilihannya ada di kamu.<br><br>

						Investasi dengan membeli grinder (penggiling) kopi yang bagus dan hasilnya konsisten dan membeli kopi dalam bentuk biji sehingga kamu bisa selalu menyeduh kopi yang segar atau<br>

						Tetap membeli kopi dalam bentuk bubuk sambil berharap pihak roastery kopi tempat kamu beli kopi memiliki grinder (penggiling) kopi yang bagus dan hasilnya konsisten.<br>
						Saran dari saya ketika beli kopi dalam bentuk bubuk, mintalah biji kopi digiling (grinder) dengan profil gilingan yang agak kasar, karena biasanya kopi bubuk dalam bentuk halus lebih cepat basi daripada bubuk kopi yang digiling kasar.
													

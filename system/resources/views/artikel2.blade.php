						<p>Ketika kamu sudah masuk ke dunia kopi gelombang ketiga atau third wave coffee, maka kamu akan mengenal beberapa rules atau ungkapan, seperti:
						“Beli kopinya dalam bentuk biji aja, nanti digilingnya ketika mau diseduh” atau “Kopi dalam bentuk biji selalu lebih segar daripada kopi yang sudah dalam bentuk bubuk”.
